/**
 * A superclass that HAS default constructor.
 * 
 * @author 3rdstage
 *
 */
public class Point1{
	
	private int x;
	
	private int y;
	
	public Point1(){
		
	}
	
	public int getX(){ return this.x; }
	public void setX(int x){ this.x = x; }
	
	public int getY(){ return this.y; }
	public void setY(int y){ this.y = y; }

}
